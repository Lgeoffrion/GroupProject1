//When button wine button is clicked 
$("#wine-button").on("click", function () {
    // If button has "More" as text, display the wine pricing details and change the button text to "Less"
    if ((this.textContent) == "More") {
        $("#wine-price-details").show();
        this.textContent = "Less";
    }
    // If button has "Less" as text, hide the wine pricing details and change the button text to "More"
    else {
        $("#wine-price-details").hide();
        this.textContent = "More";
    }
});

